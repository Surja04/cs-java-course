
package Beginnerjava;


public class IncrementDecrement {
      public static void main(String[]args){
        
        int x=7;
        int y=10;
        x=++y;
        System.out.println(x);
        System.out.println(y);
        y=x++;
        System.out.println(y);
        System.out.println(x);
        
    }
    
    
}
